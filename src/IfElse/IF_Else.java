package IfElse;

public class IF_Else {
    public static void main(String[] args) {
        int temp = 29;

        if (temp > 30) {
            System.out.println("It's a hot day,");
        } else if (temp > 20) {
            System.out.println("It's a nice day.");
        } else {
            System.out.println("It's a cold day.");
        }
    }
}